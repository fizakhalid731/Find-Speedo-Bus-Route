const dropdownlist = () => {
    let from_stations = document.getElementById("from-station");
    let to_stations = document.getElementById("to-station");

    let from_Value = from_stations.value;
    let to_Value = to_stations.value;


    for (let i = 0; i < from_stations.length; i++) {
      from_stations[i].disabled = false;
    }

    for (let i = 0; i < to_stations.length; i++) {
      to_stations[i].disabled = false;
    }

    if (from_Value) {
      for (let i = 0; i < from_stations.length; i++) {
        if (from_stations[i].value == to_Value) {
          from_stations[i].disabled = true;
        }
      }
    }

    if (to_Value) {
      for (let i = 0; i < to_stations.length; i++) {
        if (to_stations[i].value == from_Value) {
          to_stations[i].disabled = true;
        }
      }
    }
  };

// linear search
  const show_Route_with_linear_search = () => {
    let from_stations = document.getElementById("from-station");
    let to_stations = document.getElementById("to-station");

    let from_Value = from_stations.value;
    let to_Value = to_stations.value;

    let div_result = document.getElementById("result");
    div_result.innerHTML = ""; // Clear previous results


    // Use map to find routes
    let find_route = routes.map(route => {
        let from_station_Exists = false; 
        let to_station_Exists = false;
        
        route.stations.map(find_station => {
            if(find_station == from_Value){
                from_station_Exists = true;
            }

            if(find_station == to_Value){
                to_station_Exists = true;
            }
        });
         
        return from_station_Exists && to_station_Exists ? route : null;

    }).filter(route => route !== null); // Filter out null values

    // Display the results
    if (find_route.length > 0) {
        find_route.map(route => {
            div_result.innerHTML += `
            
                <h3>Route Number: ${route.routeNumber}</h3>
                <h4>From: ${from_Value} <i class="fa-solid fa-arrow-right"></i> To: ${to_Value}</h4>
                <h4>Bus Type: ${route.Type_of_bus}</h4>
                <p>
                    ${route.stations.map(station => `${station}`).join(` <i class="fa-solid fa-arrow-right"></i> `)}
                </p>
              
            `;
        });
    } else {
        div_result.innerHTML = "<p>No route found for the selected options.</p>";
    }
};




